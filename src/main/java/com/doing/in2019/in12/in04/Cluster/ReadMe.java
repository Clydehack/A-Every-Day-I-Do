package com.doing.in2019.in12.in04.Cluster;

public class ReadMe {

	/**
	 * 集群：
	 * 
	 * 	 概念：
	 * 		单个计算机性能有限，通过一组计算机，每个节点都做同样的事情，解决同一项问题的计算机群组，就是集群。
	 * 
	 * 	 特点：
	 * 		可扩展，可以随时增减计算机数量，弹性伸缩集群的性能。
	 * 		高可用，高并发可用，当集群中某节点发生故障，该节点的任务自动转移到其他节点处理，消除了单点故障。
	 * 
	 * 	 能力：
	 * 		负载均衡，把任务均匀分布到集群环境下，以提高系统吞吐量。
	 * 		错误恢复，有其他节点承担异常节点的工作，有足够时间进行异常处理
	 * 
	 */
}