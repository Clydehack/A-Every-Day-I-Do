package com.doing.in2019.in05.in19.network;

public class ReadMe {

	/**
	 * Java输入输出(I/O)建立在流上(Stream)
	 * 	1.输入流（InputStream），用于读取数据
	 * 	2.输出流（OutputStream），用于写入数据
	 * 	3.过滤器（filter），串链在输入输出流上，可以修改数据，例如加密或压缩数据
	 * 	4.阅读器（reader），串链在输入流上，允许程序读文本（即字符）数据
	 * 	5.书写器（writer），串链在输出流上，允许程序写文本（即字符）数据
	 * 
	 * 流是同步的，当线程进行一个流的输入输出时，处于阻塞状态。
	 * 非阻塞I/O在高吞吐量的应用程序中（web应用），要快得多！
	 */
}